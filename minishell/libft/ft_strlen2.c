#include"libft.h"
#include <stdio.h>

int	ft_strlen2(char *s)
{
	int	c;

	c = 0;
	if (s[c] == ' ' || s[c] == '\0')
		return (0);
	while (s[c] != 0 && (s[c] == '_' || ft_isalnum(s[c])))
		c++;
	return (c);
}
