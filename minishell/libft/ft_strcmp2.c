#include"libft.h"

int	ft_strcmp2(char *s1, char *s2)
{
	int	i;

	i = 0;
	if (!s1 || !s2)
		return (-6969);
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		else
			i++;
	}
	return (0);
}
